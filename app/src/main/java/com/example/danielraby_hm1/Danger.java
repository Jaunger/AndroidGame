package com.example.danielraby_hm1;

public class Danger {

    private int image;
    private boolean active;


    public int getImage() {
        return image;
    }

    public Danger setImage(int image) {
        this.image = image;
        return this;
    }

    public boolean isActive() {
        return active;
    }

    public Danger setActive(boolean active) {
        this.active = active;
        return this;
    }
}
